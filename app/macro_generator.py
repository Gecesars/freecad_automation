    def _indent(self, code: str, spaces: int) -> str:
        prefix = " " * spaces
        return "\n".join((prefix + line if line.strip() else line) for line in code.splitlines())

    def body_for_spec(self, spec: PartSpec) -> str:
        return {
            "plate": self._plate_body,
            "flange": self._flange_body,
            "cylinder": self._cylinder_body,
            "l_bracket": self._l_bracket_body,
            "box": self._box_body,
        }[spec.part_type](spec)

    def _deepseek_body(
        self,
        spec: PartSpec,
        rag_results: tuple[SearchResult, ...],
        deterministic_body: str,
    ) -> DeepSeekMacroSuggestion | None:
        if not self.deepseek:
            return None
        suggestion = self.deepseek.suggest_body(spec, rag_results, deterministic_body)
        if suggestion and suggestion.body_code:
            return suggestion
        return suggestion

    def _deepseek_review(
        self,
        spec: PartSpec,
        rag_results: tuple[SearchResult, ...],
        deterministic_body: str,
    ) -> DeepSeekMacroSuggestion | None:
        if not self.deepseek:
            return None
        return self.deepseek.review_spec(spec, rag_results, deterministic_body)

    def format_summary(
        self,
        spec: PartSpec,
        macro_path: Path,
        output_paths: dict[str, Path],
        llm_used: bool = False,
        llm_model: str | None = None,
        llm_notes: str = "",
    ) -> str:
        dims = ", ".join(f"{key}={format_mm(value)} mm" for key, value in sorted(spec.dimensions.items()))
        features = ", ".join(feature.kind for feature in spec.features) or "sem recursos extras"
        assumptions = "\n".join(f"- {item}" for item in spec.assumptions) or "- Nenhuma suposicao extra."
        warnings = "\n".join(f"- {item}" for item in spec.warnings) or "- Nenhum aviso."
        outputs = "\n".join(f"- {kind}: {path}" for kind, path in output_paths.items())
        return (
            f"Tipo: {spec.part_type}\n"
            f"Material: {spec.material or 'nao informado'}\n"
            f"Dimensoes: {dims}\n"
            f"Recursos: {features}\n"
            f"DeepSeek: {'usado' if llm_used else 'nao usado/fallback'}"
            f"{f' ({llm_model})' if llm_model else ''}\n"
            f"Notas DeepSeek: {llm_notes or 'sem notas'}\n"
            f"Macro: {macro_path}\n\n"
            f"Arquivos esperados:\n{outputs}\n\n"
            f"Suposicoes:\n{assumptions}\n\n"
            f"Avisos:\n{warnings}"
        )

    def _feature(self, spec: PartSpec, kind: str):
        return spec.feature(kind)

    def _holes(self, spec: PartSpec) -> tuple[int, float, str]:
        feature = self._feature(spec, "holes") or self._feature(spec, "bolt_circle_holes")
        if not feature:
            return 0, spec.dimensions.get("hole_diameter", 6.0), "default"
        return (
            int(feature.params.get("count", 0)),
            float(feature.params.get("diameter", spec.dimensions.get("hole_diameter", 6.0))),
            str(feature.params.get("placement", "default")),
        )

    def _plate_body(self, spec: PartSpec) -> str:
        d = spec.dimensions
        length, width, thickness = d["length"], d["width"], d["thickness"]
        hole_count, hole_diameter, placement = self._holes(spec)
        margin = min(d.get("margin", 10.0), length / 2.5, width / 2.5)
        hole_points = self._rect_holes(length, width, hole_count, margin, placement)
        lines = [f"shape = Part.makeBox({format_mm(length)}, {format_mm(width)}, {format_mm(thickness)}, Vector(0, 0, 0))"]
        for x, y in hole_points:
            lines.append(
                f"shape = cut_cylinder(shape, {format_mm(hole_diameter / 2)}, {format_mm(thickness + 2)}, "
                f"Vector({format_mm(x)}, {format_mm(y)}, -1), Vector(0, 0, 1))"
            )
        slot = self._feature(spec, "slot")
        if slot:
            slot_length = float(slot.params["length"])
            slot_width = float(slot.params["width"])
            lines.extend(self._slot_cut_lines(slot_length, slot_width, thickness, length / 2, width / 2))
        lines.append(self._finish_edges(spec))
        return "\n".join(line for line in lines if line)

    def _flange_body(self, spec: PartSpec) -> str:
        lines = [render_flange_body(spec), self._finish_edges(spec)]
        return "\n".join(line for line in lines if line)

    def _cylinder_body(self, spec: PartSpec) -> str:
        d = spec.dimensions
        diameter, length = d["diameter"], d["length"]
        lines = [
            f"shape = Part.makeCylinder({format_mm(diameter / 2)}, {format_mm(length)}, Vector(0, 0, 0), Vector(0, 0, 1))",
            self._finish_edges(spec),
        ]
        return "\n".join(line for line in lines if line)

    def _l_bracket_body(self, spec: PartSpec) -> str:
        d = spec.dimensions
        length, width, height, thickness = d["length"], d["width"], d["height"], d["thickness"]
        hole_count, hole_diameter, placement = self._holes(spec)
        margin = min(d.get("margin", 10.0), length / 3, width / 3, height / 3)
        lines = [
            f"base = Part.makeBox({format_mm(length)}, {format_mm(width)}, {format_mm(thickness)}, Vector(0, 0, 0))",
            f"vertical = Part.makeBox({format_mm(thickness)}, {format_mm(width)}, {format_mm(height)}, Vector(0, 0, {format_mm(thickness)}))",
            "shape = base.fuse(vertical)",
        ]
        if hole_count:
            base_count = max(1, hole_count // 2) if placement == "tabs" else min(hole_count, 2)
            vertical_count = max(1, hole_count - base_count) if placement == "tabs" else max(0, hole_count - base_count)
            for x, y in self._rect_holes(length, width, base_count, margin, "default"):
                lines.append(
                    f"shape = cut_cylinder(shape, {format_mm(hole_diameter / 2)}, {format_mm(thickness + 2)}, "
                    f"Vector({format_mm(x)}, {format_mm(y)}, -1), Vector(0, 0, 1))"
                )
            for _, y in self._rect_holes(height, width, vertical_count, margin, "default"):
                z = margin if vertical_count == 1 else y
                lines.append(
                    f"shape = cut_cylinder(shape, {format_mm(hole_diameter / 2)}, {format_mm(thickness + 2)}, "
                    f"Vector(-1, {format_mm(width / 2)}, {format_mm(thickness + z)}), Vector(1, 0, 0))"
                )
        lines.extend(self._rib_lines(length, width, height, thickness))
        lines.append(self._finish_edges(spec))
        return "\n".join(line for line in lines if line)

    def _box_body(self, spec: PartSpec) -> str:
        d = spec.dimensions
        length, width, height, wall = d["length"], d["width"], d["height"], d["wall"]
        lines = [f"shape = Part.makeBox({format_mm(length)}, {format_mm(width)}, {format_mm(height)}, Vector(0, 0, 0))"]
        if self._feature(spec, "hollow"):
            inner_length = length - 2 * wall
            inner_width = width - 2 * wall
            inner_height = height
            lines.append(
                f"inner = Part.makeBox({format_mm(inner_length)}, {format_mm(inner_width)}, {format_mm(inner_height)}, "
                f"Vector({format_mm(wall)}, {format_mm(wall)}, {format_mm(wall)}))"
            )
            lines.append("shape = shape.cut(inner)")
        lines.append(self._finish_edges(spec))
        return "\n".join(line for line in lines if line)

    def _finish_edges(self, spec: PartSpec) -> str:
        fillet = spec.feature("fillet")
        chamfer = spec.feature("chamfer")
        lines: list[str] = []
        if fillet:
            lines.append(f"shape = safe_fillet(shape, {format_mm(float(fillet.params.get('radius', 2.0)))})")
        if chamfer:
            lines.append(f"shape = safe_chamfer(shape, {format_mm(float(chamfer.params.get('distance', 1.0)))})")
        return "\n".join(lines)

    def _slot_cut_lines(
        self,
        slot_length: float,
        slot_width: float,
        thickness: float,
        center_x: float,
        center_y: float,
    ) -> list[str]:
        return [
            f"slot_width = {format_mm(slot_width)}",
            f"slot_length = {format_mm(slot_length)}",
            f"slot_height = {format_mm(thickness + 2)}",
            f"slot_x = {format_mm(center_x)}",
            f"slot_y = {format_mm(center_y)}",
            "straight = max(slot_length - slot_width, 0)",
            "slot = Part.makeBox(straight, slot_width, slot_height, Vector(slot_x - straight / 2, slot_y - slot_width / 2, -1))",
            "slot = slot.fuse(Part.makeCylinder(slot_width / 2, slot_height, Vector(slot_x - straight / 2, slot_y, -1), Vector(0, 0, 1)))",
            "slot = slot.fuse(Part.makeCylinder(slot_width / 2, slot_height, Vector(slot_x + straight / 2, slot_y, -1), Vector(0, 0, 1)))",
            "shape = shape.cut(slot)",
        ]

    def _rect_holes(
        self,
        length: float,
        width: float,
        count: int,
        margin: float,
        placement: str,
    ) -> list[tuple[float, float]]:
        if count <= 0:
            return []
        if count == 1:
            return [(length / 2, width / 2)]
        if count == 2:
            return [(margin, width / 2), (length - margin, width / 2)]
        corners = [
            (margin, margin),
            (length - margin, margin),
            (length - margin, width - margin),
            (margin, width - margin),
        ]
        if placement == "corners" or count <= 4:
            return corners[:count]
        points = corners[:]
        extra = count - 4
        for idx in range(extra):
            fraction = (idx + 1) / (extra + 1)
            points.append((margin + fraction * (length - 2 * margin), width / 2))
        return points

    def _rib_lines(self, length: float, width: float, height: float, thickness: float) -> list[str]:
        rib_width = min(max(thickness, 4.0), width * 0.18)
        y_positions = [max(thickness, width * 0.20), min(width - rib_width - thickness, width * 0.72)]
        span = min(length * 0.45, height * 0.65)
        rib_height = min(height * 0.70, length * 0.50)
        lines = [
            "def make_rib(y_pos):",
            "    wire = Part.makePolygon([",
            f"        Vector({format_mm(thickness)}, y_pos, {format_mm(thickness)}),",
            f"        Vector({format_mm(thickness + span)}, y_pos, {format_mm(thickness)}),",
            f"        Vector({format_mm(thickness)}, y_pos, {format_mm(thickness + rib_height)}),",
            f"        Vector({format_mm(thickness)}, y_pos, {format_mm(thickness)}),",
            "    ])",
            f"    return Part.Face(wire).extrude(Vector(0, {format_mm(rib_width)}, 0))",
        ]
        for y in y_positions:
            lines.append(f"shape = shape.fuse(make_rib({format_mm(y)}))")
        return lines
