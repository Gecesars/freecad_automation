def ensure_dir(path):
    os.makedirs(path, exist_ok=True)
    return path


def add_shape(doc, name, shape):
    obj = doc.addObject("Part::Feature", name)
    obj.Shape = shape
    try:
        obj.Label = name
        if MATERIAL_NAME:
            obj.Material = {{"Name": MATERIAL_NAME}}
        if hasattr(obj, "ViewObject") and obj.ViewObject:
            obj.ViewObject.ShapeColor = MATERIAL_COLOR
    except Exception as exc:
        log(f"Visual material skipped: {{exc}}")
    return obj


def cut_cylinder(shape, radius, height, base, direction):
    tool = Part.makeCylinder(radius, height, base, direction)
    return shape.cut(tool)


def safe_fillet(shape, radius):
    if radius <= 0:
        return shape
    try:
        return shape.makeFillet(radius, shape.Edges)
    except Exception as exc:
        log(f"Fillet skipped: {{exc}}")
        return shape


def safe_chamfer(shape, distance):
    if distance <= 0:
        return shape
    try:
        return shape.makeChamfer(distance, shape.Edges)
    except Exception as exc:
        log(f"Chamfer skipped: {{exc}}")
    return shape


def validate_shape(shape, name):
    info = {{"name": name, "valid": False, "volume": 0.0, "area": 0.0, "faces": 0, "edges": 0, "solids": 0, "bbox": {{}}}}
    try:
        if shape is None or shape.isNull():
            raise ValueError(name + " is null")
        try:
            shape = shape.removeSplitter()
        except Exception as exc:
            log(f"removeSplitter skipped for {{name}}: {{exc}}")
        valid = bool(shape.isValid())
        if not valid:
            try:
                shape.fix(0.01, 0.01, 0.01)
                valid = bool(shape.isValid())
            except Exception as exc:
                log(f"shape.fix skipped for {{name}}: {{exc}}")
        bbox = shape.BoundBox
        info.update(
            {{
                "valid": valid,
                "volume": float(getattr(shape, "Volume", 0.0)),
                "area": float(getattr(shape, "Area", 0.0)),
                "faces": len(getattr(shape, "Faces", [])),
                "edges": len(getattr(shape, "Edges", [])),
                "solids": len(getattr(shape, "Solids", [])),
                "bbox": {{
                    "x_length": float(bbox.XLength),
                    "y_length": float(bbox.YLength),
                    "z_length": float(bbox.ZLength),
                    "xmin": float(bbox.XMin),
                    "ymin": float(bbox.YMin),
                    "zmin": float(bbox.ZMin),
                    "xmax": float(bbox.XMax),
                    "ymax": float(bbox.YMax),
                    "zmax": float(bbox.ZMax),
                }},
            }}
        )
        if not valid:
            log(f"WARNING: {{name}} is invalid after repair attempts")
        return shape, info
    except Exception:
        log("Validation failed:\\n" + traceback.format_exc())
        raise


